
package modelo;


public class Funeraria {
    
    private int id_funeraria;
    private String razon_social;
    private String ruc;
    private String tipo_servicio;
    private String autorizacion;
    private String persona_natural;
    private String direccion;
    private String telefono;
    private String email;

    public int getId_funeraria() {
        return id_funeraria;
    }

    public void setId_funeraria(int id_funeraria) {
        this.id_funeraria = id_funeraria;
    }

    public String getRazon_social() {
        return razon_social;
    }

    public void setRazon_social(String razon_social) {
        this.razon_social = razon_social;
    }

    public String getRuc() {
        return ruc;
    }

    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    public String getTipo_servicio() {
        return tipo_servicio;
    }

    public void setTipo_servicio(String tipo_servicio) {
        this.tipo_servicio = tipo_servicio;
    }

    public String getAutorizacion() {
        return autorizacion;
    }

    public void setAutorizacion(String autorizacion) {
        this.autorizacion = autorizacion;
    }

    public String getPersona_natural() {
        return persona_natural;
    }

    public void setPersona_natural(String persona_natural) {
        this.persona_natural = persona_natural;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
                                                              
    
    
}

