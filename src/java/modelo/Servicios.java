
package modelo;


public class Servicios {
    
    private int id_servicio;
    private String cod_servicio;
    private String nombre_servicio;
    private String foto_servicio;
    private double valor_servicio;

    public int getId_servicio() {
        return id_servicio;
    }

    public void setId_servicio(int id_servicio) {
        this.id_servicio = id_servicio;
    }

    public String getCod_servicio() {
        return cod_servicio;
    }

    public void setCod_servicio(String cod_servicio) {
        this.cod_servicio = cod_servicio;
    }

    public String getNombre_servicio() {
        return nombre_servicio;
    }

    public void setNombre_servicio(String nombre_servicio) {
        this.nombre_servicio = nombre_servicio;
    }

    public String getFoto_servicio() {
        return foto_servicio;
    }

    public void setFoto_servicio(String foto_servicio) {
        this.foto_servicio = foto_servicio;
    }

    public double getValor_servicio() {
        return valor_servicio;
    }

    public void setValor_servicio(double valor_servicio) {
        this.valor_servicio = valor_servicio;
    }
    
    
  
}
